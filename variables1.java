public class variables1
{

	public static void main(String[] args)
	{
		//integer number stored in memory
		int myFirstNumber = 58;

		System.out.println("my first Number: " + myFirstNumber);

		
		
		int mySecondNumber = 32;
		System.out.println("my second Number: " + mySecondNumber);

		//basic addition
		int basicAddition = myFirstNumber + mySecondNumber;
		System.out.println("Basic addition = " + basicAddition);

		int basicMultiplication = myFirstNumber * mySecondNumber;
		System.out.println("Basic multiplication = " + basicMultiplication);


	}
}

