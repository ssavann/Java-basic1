public class ternary 
{

	public static void main(String[] args)
	{
        boolean isCar = false;

        boolean wasCar = isCar ?  true : false;
        if(wasCar)
        {
            System.out.println("wasCar is true");
        }
        else
        {
            System.out.println("wasCar is false");
        }
		
	}
}